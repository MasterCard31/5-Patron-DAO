package modelos;

/**
 *
 * @author ACER
 */
public class productos {
    private String codigo;
    private String nombre;
    private String precio;
    private String stock;

    public String getCodigo() {
        return codigo;
    }

    public productos setCodigo(String codigo) {
        this.codigo = codigo;
        return this;
    }

    public String getNombre() {
        return nombre;
    }

    public productos setNombre(String nombre) {
        this.nombre = nombre;
        return this;
    }

    public String getPrecio() {
        return precio;
    }

    public productos setPrecio(String precio) {
        this.precio = precio;
        return this;
    }

    public String getStock() {
        return stock;
    }

    public productos setStock(String stock) {
        this.stock = stock;
        return this;
    }
    
    
    
    
    
}
